                                print_url = print_url.replace(':slug', product.slug);
                                let rate = '';
                                for (var i = 1; i <= 5; i++) {
                                    if (i <= product.rate) {
                                        rate = rate + '<span class="fas fa-star text-warning"></span>';
                                    } else {
                                        rate = rate + '<span class="far fa-star"></span>';
                                    }
                                }
